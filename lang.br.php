<p>A abordagem que originou a ferramenta QualiSWBES foi desenvolvida com base em pesquisas realizadas nos modelos de avaliação de qualidade existentes na literatura, tanto os que avaliam software, sistemas educacionais Web, interface, quanto os que avaliam objetos de aprendizagem e tecnologias da Web Semântica (principalmente ontologias). </p>
<p>Além disso, foram considerados os padrões/normas internacionais ISO/IEC SQuaRE 25000 e os adotados pelo W3C que contribuíram para estabelecer e definir os fatores e critérios que compuseram a abordagem criada. </p>
<p>Por fim, por se tratar de abordagem que avalia SWBES, as Heurísticas de Nielsen e Regras de Ouro de Shneiderman também foram analisadas, pois usabilidade é uma característica da qualidade do sistema que contribui para facilitar seu uso pelos usuários, principalmente estudantes, e permite que eles possam se concentrar em aprender os conteúdos e não gastar tempo e esforço para aprender a usar a ferramenta. </p>
<p>Dessa forma, os fatores e critérios foram ajustados e se definiram os artefatos e avaliadores responsáveis pelo processo de avaliação. </p>
<ul><li>Os fatores avaliados são adaptabilidade, interoperabilidade, personalização, reusabilidade e usabilidade.</li>
<li>Os Artefatos são: ontologias, objetos de Aprendizagem, interface e software.</li>
<li>Os Avaliadores são: engenheiro de conhecimento/ontologias), desenvolvedores, autores, professores, tutores, estudantes e gestores.</li></ul>
<p>Assim, a partir de um repositório de SWBES, os interessados poderão utilizar a ferramenta para avaliar os sistemas, de acordo com os fatores de qualidade estabelecidos e realizar a escolha mais adequada às suas necessidades. </p>';


